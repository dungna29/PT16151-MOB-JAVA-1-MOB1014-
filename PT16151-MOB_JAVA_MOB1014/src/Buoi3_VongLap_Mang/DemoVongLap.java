public class DemoVongLap {

    public static void main(String[] args) {
        for (int i = 1; i <= 100; i++) {
            System.out.println("Chào bạn đến với java " + i);
        }

        int i = 1;
        while (i <= 100) {
            System.out.println("Chào bạn đến với java " + i);
            i++;
        }
        i = 1;
        do {
            System.out.println("Chào bạn đến với java " + i);
            i++;
        } while (i <= 100);
        
      
    }
}
